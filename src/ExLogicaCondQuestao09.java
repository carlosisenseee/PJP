public class ExLogicaCondQuestao09 {
    public static void main(String[] args) {
        for (int i = 0; i <= 100; i++) {
            if (i % 2 == 0) {
                System.out.println(i + " é Par");
            } else {
                System.out.println(i + " é Ímpar");
            }
        }
    }
}
